package weather;

public class City {
    private String name;
    private String asfd;

    public City() {
    }

    public City(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAsfd(String asfd) {
        this.asfd = asfd;
    }

    public String getAsfd() {
        return asfd;
    }
}
